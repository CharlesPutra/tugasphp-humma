<?php

//isi variabel dengan case yang ada
$bulan = 4;

switch ($bulan) {
    case 1:
        echo "bulan january";
        break;
    case 2:
        echo "bulan february";
        break;
    
    case 3:
        echo "bulan maret";
        break;
    
    case 4:
        echo "bulan april";
        break;
    
    case 5:
        echo "bulan mei";
        break;
    default:
    echo "bulan tidak ditemukan";
    break;
}
?>